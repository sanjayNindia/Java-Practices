package com.practice.java;

public class MahadevExample {
    public static void main(String[] args) {
        int a[] = {10,20,30,40,50,60};
        String names[] = {"Mahadev","Loves","Samantha"};
        
        for(int mahadev = 0; mahadev<a.length;mahadev++){
            System.out.print(a[mahadev]+" ");
        }
        System.out.println();
        for(int i = 0; i<names.length;i++){
            System.out.print(names[i]+" ");
        }
        System.out.println();
    }
}
